func TestComparePassword_LegacyFormat(t *testing.T) {
	// Simulate a hash generated by Node.js: scrypt$<salt>$<key> with N=16384
	password := "legacy_node_password"
	salt := make([]byte, saltLen)
	if _, err := rand.Read(salt); err != nil {
		t.Fatalf("salt generation failed: %v", err)
	}

	key, err := scrypt.Key([]byte(password), salt, scryptNLegacy, scryptR, scryptP, keyLen)
	if err != nil {
		t.Fatalf("scrypt key derivation failed: %v", err)
	}

	legacyHash := "scrypt$" + hex.EncodeToString(salt) + "$" + hex.EncodeToString(key)

	match, err := ComparePassword(password, legacyHash)
	if err != nil {
		t.Fatalf("ComparePassword failed on legacy hash: %v", err)
	}
	if !match {
		t.Error("legacy Node.js hash should verify correctly with N=16384 fallback")
	}
}

