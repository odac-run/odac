func TestHashPassword_AdaptiveFormat(t *testing.T) {
	hash, err := HashPassword("testpassword123")
	if err != nil {
		t.Fatalf("HashPassword failed: %v", err)
	}

	// New format: scrypt$<N>$<salt>$<key> → 4 parts
	parts := strings.SplitN(hash, "$", 4)
	if len(parts) != 4 {
		t.Fatalf("hash should have 4 parts separated by $, got %d", len(parts))
	}

	if parts[0] != "scrypt" {
		t.Errorf("hash should start with 'scrypt', got: %s", parts[0])
	}

	if parts[1] != "32768" {
		t.Errorf("N parameter should be 32768, got: %s", parts[1])
	}

	// Salt should be 32 hex chars (16 bytes)
	if len(parts[2]) != 32 {
		t.Errorf("salt hex should be 32 chars, got %d", len(parts[2]))
	}

	// Key should be 128 hex chars (64 bytes)
	if len(parts[3]) != 128 {
		t.Errorf("key hex should be 128 chars, got %d", len(parts[3]))
	}
}

