func TestCrossCompatibility_LegacyToAdaptive(t *testing.T) {
	// Verify the full upgrade flow:
	// 1. Legacy hash created by Node.js (N=16384)
	// 2. Go verifies it successfully
	// 3. NeedsRehash detects it
	// 4. Go creates a new adaptive hash (N=32768)
	// 5. New hash is verifiable
	password := "upgrade_test_password"

	// Step 1: Simulate Node.js legacy hash
	salt := make([]byte, saltLen)
	rand.Read(salt)
	key, _ := scrypt.Key([]byte(password), salt, scryptNLegacy, scryptR, scryptP, keyLen)
	legacyHash := "scrypt$" + hex.EncodeToString(salt) + "$" + hex.EncodeToString(key)

	// Step 2: Verify legacy hash
	match, err := ComparePassword(password, legacyHash)
	if err != nil || !match {
		t.Fatal("legacy hash should verify successfully")
	}

	// Step 3: Detect rehash need
	if !NeedsRehash(legacyHash) {
		t.Fatal("legacy hash should need rehash")
	}

	// Step 4: Create new adaptive hash
	newHash, err := HashPassword(password)
	if err != nil {
		t.Fatalf("HashPassword failed: %v", err)
	}

	// Step 5: Verify new hash
	match, err = ComparePassword(password, newHash)
	if err != nil || !match {
		t.Fatal("new adaptive hash should verify successfully")
	}

	// Step 6: New hash should NOT need rehash
	if NeedsRehash(newHash) {
		t.Fatal("new adaptive hash should NOT need rehash")
	}
}
